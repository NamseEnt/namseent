// 메시지 선택 헬퍼 함수
export function getRandomVirtualMessage(): string {
  return VIRTUAL_USER_MESSAGES[Math.floor(Math.random() * VIRTUAL_USER_MESSAGES.length)];
}

// 시간대별 메시지 필터링 (옵션)
export function getTimeBasedMessage(): string {
  const hour = new Date().getHours();
  let messages = VIRTUAL_USER_MESSAGES;
  
  // 시간대별 특화 메시지 우선 선택
  if (hour >= 0 && hour <= 5) {
    // 새벽 시간대
    const lateNightMessages = messages.filter(msg => 
      msg.includes('새벽') || msg.includes('밤샘') || msg.includes('이 시간')
    );
    if (lateNightMessages.length > 0) {
      return lateNightMessages[Math.floor(Math.random() * lateNightMessages.length)];
    }
  }
  
  return getRandomVirtualMessage();
}